    private void cargarDatosUsuario() {
    try {
        connect cn = new connect();
        Connection con = cn.connect();

        String sql = "SELECT * FROM usuarios WHERE id = ?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, user.getId());

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            jTextField1.setText(rs.getString("nombre"));
            jTextField2.setText(rs.getString("usuario"));
            jTextField3.setText(rs.getString("correo"));
            jTextField4.setText(String.valueOf(rs.getInt("id"))); // no modificable
            jTextField5.setText(rs.getString("carrera"));
        }

        rs.close();
        ps.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error cargando datos: " + e.toString());
    }
}

