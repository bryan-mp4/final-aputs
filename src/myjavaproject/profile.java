    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String nombre = jTextField1.getText();
    String usuario = jTextField2.getText();
    String correo = jTextField3.getText();
    String carrera = jTextField5.getText();
    int id = user.getId();   // el usuario logueado

    try {
        connect cn = new connect();
        Connection con = cn.connect();

        String sql = "UPDATE usuarios SET nombre = ?, usuario = ?, correo = ?, carrera = ? WHERE id = ?";

        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, nombre);
        ps.setString(2, usuario);
        ps.setString(3, correo);
        ps.setString(4, carrera);
        ps.setInt(5, id);

        int rows = ps.executeUpdate();

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Datos actualizados correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar");
        }

        ps.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al modificar: " + e.toString());
    }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int confirm = JOptionPane.showConfirmDialog(
            this,
            "¿Estás seguro de eliminar tu cuenta? Esta acción no se puede deshacer.",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {

        Connection cn = null;
        PreparedStatement pst = null;
         try {
            connect con = new connect();
            cn = con.connect();

            String sql = "DELETE FROM usuarios WHERE id = ?";
            pst = cn.prepareStatement(sql);
            user u = new user();
            pst.setInt(1, user.id);

            int resultado = pst.executeUpdate();

            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.");

                // Cerrar esta ventana
                this.dispose();

                // Volver a Login
                login login = new login();
                login.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error: no se encontró el usuario a eliminar.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
        } finally {
            try {
                if (pst != null) pst.close();
                if (cn != null) cn.close();
            } catch (Exception e) {}
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

