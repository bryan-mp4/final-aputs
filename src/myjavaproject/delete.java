    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         int fila = jTable1.getSelectedRow();

        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro primero");
            return;
        }

        int idAput = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(null,
                "¿Seguro que deseas eliminar este Aput?",
                "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) return;

        connect cn = new connect();
        Connection con = cn.connect();

        try {
            PreparedStatement ps = con.prepareStatement(
                "DELETE FROM aputs WHERE id_aput = ?"
            );

            ps.setInt(1, idAput);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Aput eliminado correctamente");

            cargarAputs();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error eliminando: " + e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

