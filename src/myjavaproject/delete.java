     public void cargarAputs() {
        DefaultTableModel modelo = new DefaultTableModel();
        jTable1.setModel(modelo);

        modelo.addColumn("ID");
        modelo.addColumn("Título");
        modelo.addColumn("Evento");
        modelo.addColumn("Apuesta");
        modelo.addColumn("Cuota");
        modelo.addColumn("Apostado");
        modelo.addColumn("Ganancia");
        modelo.addColumn("Estado");

        connect con = new connect();
        Connection cn = con.connect();

        String sql = "SELECT * FROM aputs WHERE id_usuario = ?";

        try {

            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setInt(1, user.id);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id_aput"),
                    rs.getString("titulo"),
                    rs.getString("evento"),
                    rs.getString("apuesta"),
                    rs.getDouble("cuota"),
                    rs.getDouble("apostado"),
                    rs.getDouble("ganancia"),
                    rs.getString("estado")
                });
            }

            rs.close();
            ps.close();
            cn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar: " + e);
        }
    }

     private void activarModoOscuro() {

    // Fondos
    jPanel1.setBackground(new Color(25, 25, 25));
    jPanel2.setBackground(new Color(15, 15, 15));
    jPanel3.setBackground(new Color(30, 30, 30));

    // Labels
    JLabel[] labels = { jLabel1, jLabel3 };
    for (JLabel l : labels) {
        l.setForeground(Color.WHITE);
    }

    // Tabla
    jTable1.setBackground(new Color(40, 40, 40));
    jTable1.setForeground(Color.WHITE);
    jTable1.setSelectionBackground(new Color(70, 70, 70));
    jTable1.setSelectionForeground(Color.WHITE);
    jTable1.getTableHeader().setBackground(new Color(20, 20, 20));
    jTable1.getTableHeader().setForeground(Color.WHITE);

    // Botones
    JButton[] botones = { jButton1, jButton2, jButtonDark, jButtonLight };
    for (JButton b : botones) {
        b.setBackground(new Color(70, 130, 180));
        b.setForeground(Color.WHITE);
    }

    JOptionPane.showMessageDialog(this, "Modo oscuro activado");
}
