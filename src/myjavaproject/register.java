    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String nombre = jTextField1.getText();
    String usuario = jTextField2.getText();
    String correo = jTextField3.getText();
    String contrasena = jTextField4.getText();
    String carrera = jTextField5.getText();

    if (nombre.isEmpty() || usuario.isEmpty() || correo.isEmpty() || contrasena.isEmpty() || carrera.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe llenar todos los campos");
        return;
    }

    try {
        connect cn = new connect();
        Connection con = cn.connect();

        String sql = "INSERT INTO usuarios (nombre, usuario, correo, contrasena, carrera) VALUES (?, ?, ?, ?, ?)";

        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, nombre);
        ps.setString(2, usuario);
        ps.setString(3, correo);
        ps.setString(4, contrasena);
        ps.setString(5, carrera);

        int rows = ps.executeUpdate();

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Usuario registrado correctamente");

            // Opcional: volver al login después del registro
            login l = new login();
            l.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo registrar el usuario");
        }

        ps.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al registrar: " + e.toString());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

