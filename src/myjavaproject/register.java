    private void ajustarImagen(JLabel jLabel2, String ruta ){
    this.imagen = new ImageIcon(ruta);
    this.icono = new ImageIcon(this.imagen.getImage().getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(),Image.SCALE_DEFAULT));
    jLabel2.setIcon(this.icono);
    this.repaint();
    }
    private void activarModoOscuro() {

    // Panels principales
    jPanel1.setBackground(new Color(25, 25, 25));
    jPanel2.setBackground(new Color(15, 15, 15));
    jPanel3.setBackground(new Color(30, 30, 30));
    jPanel4.setBackground(new Color(30, 30, 30));

    // Labels (texto blanco)
    JLabel[] labels = {
        jLabel1, jLabel4, jLabel5, jLabel6,
        jLabel7, jLabel8
    };
    for (JLabel l : labels) {
        l.setForeground(Color.WHITE);
    }

    // TextFields (fondo oscuro, texto blanco)
    JTextField[] campos = {
        jTextField1, jTextField2, jTextField3,
        jTextField4, jTextField5
    };
    for (JTextField t : campos) {
        t.setBackground(new Color(50, 50, 50));
        t.setForeground(Color.WHITE);
        t.setCaretColor(Color.WHITE);
    }

    // Botones
    JButton[] botones = { jButton1, jButton2, jButtonDark, jButtonLight };
    for (JButton b : botones) {
        b.setBackground(new Color(70, 130, 180));
        b.setForeground(Color.WHITE);
    }

    JOptionPane.showMessageDialog(this, "Modo oscuro activado");
}
private void activarModoClaro() {

    // Panels
    jPanel1.setBackground(Color.WHITE);
    jPanel2.setBackground(new Color(195, 215, 48));
    jPanel3.setBackground(Color.WHITE);
    jPanel4.setBackground(Color.WHITE);

    // Label del header siempre va blanco
    jLabel1.setForeground(Color.WHITE);

    // Otros labels vuelven a negro
    JLabel[] labels = {
        jLabel4, jLabel5, jLabel6, jLabel7, jLabel8
    };
    for (JLabel l : labels) {
        l.setForeground(Color.BLACK);
    }

    // Campos de texto
    JTextField[] campos = {
        jTextField1, jTextField2, jTextField3,
        jTextField4, jTextField5
    };
    for (JTextField t : campos) {
        t.setBackground(Color.WHITE);
        t.setForeground(Color.BLACK);
        t.setCaretColor(Color.BLACK);
    }

    // Botones regresan a UI por defecto
    JButton[] botones = { jButton1, jButton2, jButtonDark, jButtonLight };
    for (JButton b : botones) {
        b.setBackground(UIManager.getColor("Button.background"));
        b.setForeground(Color.BLACK);
    }

    JOptionPane.showMessageDialog(this, "Modo claro restaurado");
}


