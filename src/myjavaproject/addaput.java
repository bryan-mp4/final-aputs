    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String titulo   = jTextField1.getText();
    String evento   = jTextField2.getText();
    String apuesta  = jTextField3.getText();
    double cuota    = Double.parseDouble(jTextField4.getText());
    double apostado = Double.parseDouble(jTextField5.getText());
    double ganancia = Double.parseDouble(jTextField6.getText());
    String estado   = jComboBox1.getSelectedItem().toString();
    int id_usuario  = user.id;
    
    connect c = new connect();
    Connection con = c.connect();
    String sql = "INSERT INTO aputs (titulo, evento, apuesta, cuota, apostado, ganancia, estado, id_usuario) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    try {
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, titulo);
        ps.setString(2, evento);
        ps.setString(3, apuesta);
        ps.setDouble(4, cuota);
        ps.setDouble(5, apostado);
        ps.setDouble(6, ganancia);
        ps.setString(7, estado);
        ps.setInt(8, id_usuario);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Aput agregado correctamente.");

        // Limpiar campos
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        //jTextField7.setText("");
        jComboBox1.setSelectedIndex(0);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al insertar: " + e);
    }


    }//GEN-LAST:event_jButton1ActionPerformed

