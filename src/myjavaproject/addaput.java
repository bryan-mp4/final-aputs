    private void activarModoOscuro() {

    // Fondos
    jPanel1.setBackground(new Color(30, 30, 30));
    jPanel2.setBackground(new Color(20, 20, 20));
    jPanel3.setBackground(new Color(35, 35, 35));

    // Labels
    JLabel[] labels = {
        jLabel1, jLabel2, jLabel4, jLabel5, jLabel6,
        jLabel7, jLabel8, jLabel9, jLabel10
    };
    for (JLabel l : labels) {
        l.setForeground(Color.WHITE);
    }

    // TextFields
    JTextField[] fields = {
        jTextField1, jTextField2, jTextField3,
        jTextField4, jTextField5, jTextField6
    };
    for (JTextField t : fields) {
        t.setBackground(new Color(60, 60, 60));
        t.setForeground(Color.WHITE);
        t.setCaretColor(Color.WHITE);
        t.setBorder(BorderFactory.createLineBorder(new Color(120,120,120)));
    }

    // ComboBox
    jComboBox1.setBackground(new Color(60, 60, 60));
    jComboBox1.setForeground(Color.WHITE);

    // Botones
    JButton[] botones = { jButton1, jButton2, jButton3, jButtonDark, jButtonLight };
    for (JButton b : botones) {
        b.setBackground(new Color(70,130,180));
        b.setForeground(Color.WHITE);
    }

    JOptionPane.showMessageDialog(this, "Modo oscuro activado");
}
private void activarModoClaro() {

    jPanel1.setBackground(Color.WHITE);
    jPanel2.setBackground(new Color(198,215,48));
    jPanel3.setBackground(Color.WHITE);

    JLabel[] labels = {
        jLabel1, jLabel2, jLabel4, jLabel5, jLabel6,
        jLabel7, jLabel8, jLabel9, jLabel10
    };
    for (JLabel l : labels) {
        l.setForeground(Color.BLACK);
    }

    jLabel1.setForeground(Color.WHITE); // el logo del header sigue blanco

    JTextField[] fields = {
        jTextField1, jTextField2, jTextField3,
        jTextField4, jTextField5, jTextField6
    };
    for (JTextField t : fields) {
        t.setBackground(Color.WHITE);
        t.setForeground(Color.BLACK);
        t.setCaretColor(Color.BLACK);
        t.setBorder(UIManager.getBorder("TextField.border"));
    }

    jComboBox1.setBackground(Color.WHITE);
    jComboBox1.setForeground(Color.BLACK);

    JButton[] botones = { jButton1, jButton2, jButton3, jButtonDark, jButtonLight };
    for (JButton b : botones) {
        b.setBackground(UIManager.getColor("Button.background"));
        b.setForeground(Color.BLACK);
    }

    JOptionPane.showMessageDialog(this, "Modo claro listo");
}


